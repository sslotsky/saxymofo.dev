export function useMDXComponents() {

  return {
    CopyButton: component$((props: { rawText: string }) => {
      const copied = useSignal(false);
      useTask$(({ track }) => {
        track(() => copied.value);

        if (copied.value) {
          setTimeout(() => {
            copied.value = false;
          }, 2500)
        }
      });

      return (
        <button 
          onClick$={() => {
            copied.value = true;
            copyToClipboard(props.rawText)
          }}
          {...props} 
          class={`copy ${copied.value ? 'copied' : ''}`}
        >
          {copied.value ? (
            <Check />
          ) : (
            <Clipboard />
          )}
        </button>
      )
    })
  }
}